    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        int opt = JOptionPane.showConfirmDialog(null, "Deseja emitir nota fiscal?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (opt == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, "Clique no botão \"Gerar nota "
                    + "fiscal\" para emitir a nota e cadastrar a Ordem de Serviço.", 
                    "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if(!txtEquip.getText().equals("") && !txtDef.equals("") && !txtServ.getText().equals("") && tipoOS != 0) {
                   ClienteBean cb1 = (ClienteBean) txtCli.getSelectedObject();
                   int idcliente = 0;
                   idcliente = cb1.getId();
                if (idcliente == 0) {
                    JOptionPane.showMessageDialog(null, "Selecione o cliente.", "Atenção", JOptionPane.WARNING_MESSAGE);
                } else {
                    OSBean ob1 = new OSBean();
                    ManipulaDataHora mdh = new ManipulaDataHora();
                    ob1.setIdcliente(cb1.getId());
                    ob1.setTipo(tipoOS);
                    ob1.setSituacao((String) cbSitu.getSelectedItem());
                    ob1.setEquipamento(txtEquip.getText());
                    ob1.setDefeito(txtDef.getText());
                    ob1.setServico(txtServ.getText());
                    if (txtVlr.getText().isEmpty()){
                       ob1.setValor(new BigDecimal("0.00")); 
                    } else {
                        ValidationUtil vu = new ValidationUtil();
                        ob1.setValor(new BigDecimal(vu.validaPreco(txtVlr.getText())));
                    }
                    System.out.print("ID: "+cb1.getId());
                    System.out.println("Nome: "+cb1.getNome());
                    ob1.setTemntos("N");
                    /*if (OSController.create(ob) > 0) {
                        JOptionPane.showMessageDialog(null, "OS Cadastrada com sucesso.", "Registro inserido.",JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Confira as informações inseridas.", "Atenção",JOptionPane.WARNING_MESSAGE);
                    }*/
                }
            } else {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios.","Campos em branco.",JOptionPane.WARNING_MESSAGE);
            }
          }
        
    }//GEN-LAST:event_btnCadActionPerformed

