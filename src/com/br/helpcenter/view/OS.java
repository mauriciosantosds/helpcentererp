    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        OSBean ob1 = new OSBean();
        ManipulaDataHora mdh = new ManipulaDataHora();
        ob1.setNumero(ob.getNumero());
        ob1.setDataOS(mdh.getDataHoraUS(ob.getDataOS()));
        ob1.setSituacao((String) cbSitu.getSelectedItem());
        ob1.setTipo(tipoOS);
        ob1.setIdcliente(ob.getIdcliente());
        ob1.setEquipamento(txtEquip.getText());
        ob1.setDefeito(txtDef.getText());
        ob1.setServico(txtServ.getText());
        ValidationUtil vu = new ValidationUtil();
        ob1.setValor(new BigDecimal(vu.validaPreco(txtVlr.getText())));
        if (OSController.update(ob1) > 0) {
            osRel.populaTabOSRel();
            JOptionPane.showMessageDialog(null, "OS alterada");
        }
    }//GEN-LAST:event_btnAltActionPerformed

