    private void btnRemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemActionPerformed
        OSBean ob1 = notaOSTableModel.getLinhas().get(tbNotaOS.getSelectedRow());
        total = total.subtract(ob1.getValor());
        qtd = qtd -1;
        txtQtd.setText(String.valueOf(qtd));
        txtTot.setText(String.valueOf(total));
        notaOSTableModel.removeRow(tbNotaOS.getSelectedRow());
        tbNotaOS.setModel(notaOSTableModel);
    }//GEN-LAST:event_btnRemActionPerformed

    private void btnEncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncActionPerformed
        CabNoBean cnb = new CabNoBean();
        cnb.setIdCliente(ob.getIdcliente());
        cnb.setQuantidade(qtd);
        cnb.setTotalCabNota(total);
        cnb.seteOSCabNota("S");
        cnb.setOsS(osS);
        if (NotaOSController.create(cnb) > 0) {
            JOptionPane.showMessageDialog(null, "Nota criada");
        }
    }//GEN-LAST:event_btnEncActionPerformed

