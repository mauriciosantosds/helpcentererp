    //adiciona Bean de OSBean a nota
    protected void adicionaOS(OSBean ob) {
        if (qtd == 0) {
            txtCli.setText(ob.getClienome());
        }
        idos = ob.getNumero();
        qtd = qtd +1; //incrementa 1 ao contador de quantidade
        txtQtd.setText(String.valueOf(qtd)); //exibe quantidade na tela
        if(total == null) { //se a variavel total for null inicializa a variavel com 0
            total = new BigDecimal("0");
        }
        total = total.add(ob.getValor()); //soma total
        txtTot.setText(String.valueOf(total)); //exibe soma total
        osS.add(ob); //adiciona Bean de OSBean ao list de osS
        notaOSTableModel.addRow(ob); //adiciona linha na JTable de nota
    }
    
