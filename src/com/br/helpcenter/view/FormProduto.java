    //cadastra produto na base de dados
    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        ProdutoBean pb1 = new ProdutoBean();
        ValidationUtil vu = new ValidationUtil(); //instancia objeto ValidationUtil
        pb1.setEan(txtEan.getText());
        pb1.setDesc(txtDesc.getText());
        pb1.setQtd(Integer.valueOf(txtQtd.getText()));
        pb1.setPreco(new BigDecimal(vu.validaPreco(txtPre.getText()))); //aciona metodo para validar preco 
                                                                        //e e converte para BigDecimal
        if(ProdutoController.create(pb1) > 0) { //aciona metodo para persistir produto na base de dados
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso!");
        }
        
    }//GEN-LAST:event_btnCadActionPerformed

