    //evento responsavel por formatar dinheiro
    private void txtPreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPreKeyReleased
        String prec = txtPre.getText(); 
        ValidationUtil vu = new ValidationUtil();
        String valor = vu.formatMoney(prec);
        if(valor != null) {
            txtPre.setText(valor);
        }
    }//GEN-LAST:event_txtPreKeyReleased
    //evento responsavel por nao permitir insercao de outros caracteres fora numeros
    private void txtQtdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQtdKeyReleased
        String qtd = txtQtd.getText();
        String formatada = qtd.replaceAll("[^0-9]", "");
        txtQtd.setText(formatada);
    }//GEN-LAST:event_txtQtdKeyReleased
    //altera produto de acordo com id selecionado
    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        ProdutoBean pb1 = new ProdutoBean();
        ValidationUtil vu = new ValidationUtil(); //instancia objeto responsavel por validar preco
        pb1.setId(id);
        pb1.setEan(txtEan.getText());
        pb1.setDesc(txtDesc.getText());
        pb1.setPreco(new BigDecimal(vu.validaPreco(txtPre.getText()))); //aciona metodo para validar preco
                                                                        //e converte para BigDecimal
        pb1.setQtd(Integer.valueOf(txtQtd.getText()));
        if (ProdutoController.update(pb1) > 0) {
            JOptionPane.showMessageDialog(null, "Registro alterado!");
        }
    }//GEN-LAST:event_btnAltActionPerformed

