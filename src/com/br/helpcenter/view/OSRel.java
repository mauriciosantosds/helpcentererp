    private void btnNotaOSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNotaOSActionPerformed
       int linha = tabOSRel.getSelectedRow();
       if (linha != -1) {
           int id = (int) tabOSRel.getValueAt(tabOSRel.getSelectedRow(), 0); 
           OSBean ob1 = osRelTableModel.getLinhas().get(id);
           ntOs = new NotaOS();
           ntOs.adicionaOS(ob1);
           ntOs.setVisible(true);
       }
    }//GEN-LAST:event_btnNotaOSActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       int linha = tabOSRel.getSelectedRow();
       if (linha != -1) {
           int id = (int) tabOSRel.getValueAt(tabOSRel.getSelectedRow(), 0); 
           OSBean ob1 = osRelTableModel.getLinhas().get(id);    
           if(ntOs.idos != ob1.getNumero()) {
               this.dispose();
               ntOs.adicionaOS(ob1);
               ntOs.setVisible(true);
           } else {
               JOptionPane.showMessageDialog(null, "Item j√° adicionado!", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
           }
       }
    }//GEN-LAST:event_btnAddActionPerformed

