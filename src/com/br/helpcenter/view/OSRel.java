    //consulta todas os OSs
    protected void populaTabOSRel() {
        osS = OSRelController.read(); //adiciona todas as OSs ao list global de osS
                                      //para trabalhar com a filtragem nos eventos de ItemStateChanged dos radios buttons 
        osRelTableModel.addListaOS(osS); //adiciona todas as OSs no table model
        tabOSRel.setModel(osRelTableModel);
        sorter = new TableRowSorter<TableModel>(osRelTableModel); //permite a filtragem das linhas
        tabOSRel.setRowSorter(sorter); //seta RowSorter
        tabOSRel.getColumnModel().getColumn(0).setMinWidth(0); //oculta coluna 0 da Jtable
        tabOSRel.getColumnModel().getColumn(0).setMaxWidth(0); //oculta coluna 0 da Jtable
        tabOSRel.getColumnModel().getColumn(10).setMinWidth(0); //oculta coluna 10 da Jtable
        tabOSRel.getColumnModel().getColumn(10).setMaxWidth(0); //oculta coluna 10 da Jtable
    }
    //consulta OSs de acordo com periodo selecionado
    protected void populaTabOSRel(String dtIni, String dtFin, String notaOS) {
        if(!OSRelController.read(dtIni, dtFin, notaOS).isEmpty()) {//verifica se nao esta vazio
            osRelTableModel.addListaOS(OSRelController.read(dtIni, dtFin, notaOS)); //adiciona lista de OSs
            tabOSRel.setRowSorter(sorter); //permite a filtragem das linhas da tabela
        }
    }
    //exibe OSs de acordo com o marcado no radio button
    private void populaTabOSRel(List<OSBean> oSs) {
        osRelTableModel.addListaOS(oSs);
        TableModel tbModel = osRelTableModel;
        tabOSRel.setModel(tbModel);
        sorter = new TableRowSorter<TableModel>(tbModel);
        tabOSRel.setRowSorter(sorter);
        tabOSRel.getColumnModel().getColumn(0).setMinWidth(0);
        tabOSRel.getColumnModel().getColumn(0).setMaxWidth(0);
    }
    
    

