    //checkbox de verificacao se cpf vai ficar vazio
    private void checkVazioCPFStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_checkVazioCPFStateChanged
        if(checkVazioCPF.isSelected()) {
           cpfVazio = 1;
           txtCPF.setEnabled(false);
        } else {
            cpfVazio = 0;
            txtCPF.setEnabled(true);
        }
        
    }//GEN-LAST:event_checkVazioCPFStateChanged
    //checkbox de verificacao se telefone vai ficar vazio
    private void checkVazioTelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_checkVazioTelStateChanged
        if(checkVazioTel.isSelected()) {
            txtFone.setEnabled(false);
            foneVazio = 1;
        } else {
            txtFone.setEnabled(true);
            foneVazio = 0;
        }
    }//GEN-LAST:event_checkVazioTelStateChanged
    //checkbox de verificacao se e-mail vai ficar vazio
    private void checkEMailVazioStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_checkEMailVazioStateChanged
        if(checkEMailVazio.isSelected()) {
            txtEmai.setEnabled(false);
            emailVazio = 1;
        } else {
            txtEmai.setEnabled(true);
            emailVazio = 0;
        }
    }//GEN-LAST:event_checkEMailVazioStateChanged

