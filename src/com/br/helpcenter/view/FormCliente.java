    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        ClienteBean cb1 = new ClienteBean();
        ValidationUtil vu = new ValidationUtil();
        cb1.setNome(txtNome.getText());
        cb1.setFoneUm(txtCel.getText());
        //verifica se campos estao vazios e insere String em branco caso estiverem
        if(foneVazio == 0) {
            cb1.setFoneDois(txtFone.getText());
        } else {
            cb1.setFoneDois("");
        }
        if(emailVazio == 0) {
            cb1.setEmail(txtEmai.getText());
        } else {
            cb1.setEmail("");
        }
        cb1.setEndereco(txtEnd.getText());
        if(cpfVazio == 0) {
            cb1.setCPF(txtCPF.getText());
        } else {
            cb1.setCPF("");
        }
        //-----------------------------------------------------------------------
        if (ClienteController.create(cb1) > 0) {
            JOptionPane.showMessageDialog(null, "Registro inserido!");
            if(p!=null){
                //atualiza combo da tela principal
                p.carregaComboBox("Todos");   
            }
            if(os!=null){
                //atualiza combo da tela de os
                os.carregaComboBox("Todos");
            }
        }
    }//GEN-LAST:event_btnCadActionPerformed

    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        //altera informações de cliente
        ClienteBean cb1 = new ClienteBean();
        ValidationUtil vu = new ValidationUtil();
        cb1.setId(id);
        cb1.setNome(txtNome.getText());
        cb1.setFoneUm(vu.removeMaskTel(txtCel.getText()));
        cb1.setFoneDois(vu.removeMaskTel(txtFone.getText()));
        cb1.setEmail(txtEmai.getText());
        cb1.setEndereco(txtEnd.getText());
        if (ClienteController.update(cb1) > 0) {
            JOptionPane.showMessageDialog(null, "Registro atualizado!");
        }
    }//GEN-LAST:event_btnAltActionPerformed

